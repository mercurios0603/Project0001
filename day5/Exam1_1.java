package day5;

public class Exam1_1 {
        // 문제 : 일반변수에 값 할당과정 설명

        // 풀이 : int x는 main 메서드에 지역 변수 x를 선언한 것

        // JAVA 프로그램에서 main 메서드는 프로그램이 시작되는 지점이며,
        // Java Virtual Machine (JVM)이 프로그램을 실행할 때 제일 먼저 호출하는 메서드입니다.
        // // 이것은 Java 언어의 문법적 약속이며, Java 프로그램의 진입점(entry point)입니다.

        public static void main(String[] args) {
            // 설명
            int x;

            // 설명
            x = 20;

            System.out.println(x);
        }
}
